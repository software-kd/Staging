$(function () {
    try {
        var minPrice = parseFloat($('#MinPrice').val());
        var maxPrice = parseFloat($('#MaxPrice').val());
        var SelectedMinPrice = parseFloat($('#SelectedMinPrice').val());
        var SelectedMaxPrice = parseFloat($('#SelectedMaxPrice').val());

        $("#slider-range").slider({
            range: true,
            step: 0.01,
            min: minPrice,
            max: maxPrice,
            values: [SelectedMinPrice, SelectedMaxPrice],
            change: function(event, ui){
                $('#StartFilter').val('trigger timer'); 
            },
            slide: function (event, ui) {
                $("#amount").html("MYR<span>" + ui.values[0] + "</span> - MYR<span>" + ui.values[1] + "</span>");
                $('#SelectedMinPrice').val(ui.values[0]);
                $('#SelectedMaxPrice').val(ui.values[1]);
            }
        });
        $("#amount").html("MYR<span>" + $("#slider-range").slider("values", 0) +
         "</span> - MYR<span>" + $("#slider-range").slider("values", 1) + "</span>");

    } catch (e) {
        alert(e.message);
    }
});